package com.interfactura.firmalocal.xml.ecb;

public class Cobranza {

	public String fechaCobro;
	public String nombreDeudorCob;
	public String rfcDeudorCob;
	public String fechaCesion;
	public String noDocumento;
	public String valorNominalCon;
	public String totalCobrado;
	
	
	
	public String getFechaCobro() {
		return fechaCobro;
	}
	public void setFechaCobro(String fechaCobro) {
		this.fechaCobro = fechaCobro;
	}
	public String getNombreDeudorCob() {
		return nombreDeudorCob;
	}
	public void setNombreDeudorCob(String nombreDeudorCob) {
		this.nombreDeudorCob = nombreDeudorCob;
	}
	public String getRfcDeudorCob() {
		return rfcDeudorCob;
	}
	public void setRfcDeudorCob(String rfcDeudorCob) {
		this.rfcDeudorCob = rfcDeudorCob;
	}
	public String getFechaCesion() {
		return fechaCesion;
	}
	public void setFechaCesion(String fechaCesion) {
		this.fechaCesion = fechaCesion;
	}
	public String getNoDocumento() {
		return noDocumento;
	}
	public void setNoDocumento(String noDocumento) {
		this.noDocumento = noDocumento;
	}
	public String getValorNominalCon() {
		return valorNominalCon;
	}
	public void setValorNominalCon(String valorNominalCon) {
		this.valorNominalCon = valorNominalCon;
	}
	public String getTotalCobrado() {
		return totalCobrado;
	}
	public void setTotalCobrado(String totalCobrado) {
		this.totalCobrado = totalCobrado;
	}
	
	
	
	
}
